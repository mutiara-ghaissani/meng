# Indexing mrpkn mekanisme ubtuk mengambil satu item dr sbeuah data dl bntk iterable object (list, tuple, dsb)
# Indexing terbagi mnjd 2, Positive Indexing dan Negative Indexing
# Positive Indexing: dimulai dr 0, arahnya ke kanan
# Negative Indexing: dimulai dr -1, arahnya ke kiri

numbers = range(1, 21)
# [1, 2, 3, ..., 20]

# Positive Indexing (mengambil item dr kiri ke kanan)
print(numbers[4])
# output:5

# Negative indexing (mengambil item dr kanan ke kiri)
print(numbers[-3])
# output: 18


